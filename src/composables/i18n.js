function getLocaleValue(locale) {
  return locale === 'it' ? 'it' : 'en';
}

function resolveMessage(locale, key) {
  const normalizedLocale = getLocaleValue(locale);
  return translations[normalizedLocale][key] ?? translations.it[key] ?? key;
}

export function createI18nContext(localeRef) {
  const activeLocale = computed(() => getLocaleValue(localeRef.value));

  function t(key, params = {}) {
    const template = resolveMessage(activeLocale.value, key);
    return Object.entries(params).reduce((message, [paramKey, paramValue]) => {
      return message.replaceAll(`{${paramKey}}`, String(paramValue));
    }, template);
  }

  function formatDateTime(value) {
    return new Intl.DateTimeFormat(activeLocale.value === 'it' ? 'it-IT' : 'en-US', {
      dateStyle: 'short',
      timeStyle: 'medium'
    }).format(new Date(value));
  }

  function formatDateLabel(value) {
    return new Intl.DateTimeFormat(activeLocale.value === 'it' ? 'it-IT' : 'en-US', {
      weekday: 'long',
      year: 'numeric',
      month: 'long',
      day: 'numeric'
    }).format(new Date(value));
  }

  function formatCurrency(value) {
    return new Intl.NumberFormat(activeLocale.value === 'it' ? 'it-IT' : 'en-US', {
      style: 'currency',
      currency: 'EUR'
    }).format(Number(value || 0));
  }

  return {
    locale: activeLocale,
    localeOptions,
    t,
    formatDateTime,
    formatDateLabel,
    formatCurrency
  };
}

export function useI18n() {
  const context = inject(I18N_KEY, null);

  if (!context) {
    throw new Error('i18n context not provided');
  }

  return context;
}
